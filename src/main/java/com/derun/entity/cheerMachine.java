package com.derun.entity;

public class cheerMachine {

	private String gunCode;		//加油机编号
	private String machineCode;	//机号
	private String jarCode;		//对应罐号
	private String machineType;	//机型
	private String alleywayMark;	//通道号
	public String getGunCode() {
		return gunCode;
	}
	public void setGunCode(String gunCode) {
		this.gunCode = gunCode;
	}
	public String getMachineCode() {
		return machineCode;
	}
	public void setMachineCode(String machineCode) {
		this.machineCode = machineCode;
	}
	public String getJarCode() {
		return jarCode;
	}
	public void setJarCode(String jarCode) {
		this.jarCode = jarCode;
	}
	public String getMachineType() {
		return machineType;
	}
	public void setMachineType(String machineType) {
		this.machineType = machineType;
	}
	public String getAlleywayMark() {
		return alleywayMark;
	}
	public void setAlleywayMark(String alleywayMark) {
		this.alleywayMark = alleywayMark;
	}
}
