package com.derun.entity;

public class YearJarReport {
	
	private String  month;
	private double tank1;
	private double tank2;
	private double tank3;
	private double total;
	
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public double getTank1() {
		return tank1;
	}
	public void setTank1(double tank1) {
		this.tank1 = tank1;
	}
	public double getTank2() {
		return tank2;
	}
	public void setTank2(double tank2) {
		this.tank2 = tank2;
	}
	public double getTank3() {
		return tank3;
	}
	public void setTank3(double tank3) {
		this.tank3 = tank3;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
}
